    <h3>Déroulement du concours et inscription</h3>
    <p> Concours de chant organisé par l’office de la culture de la mairie de Longuenesse. Le concours se déroulera dans l'après-midi et la soirée du samedi suivant 15 jours après la clôture des inscriptions en ligne. afin de permettre aux organisateurs, techniciens, secrétariat de finaliser les détails. L’inscription s’effectuera obligatoirement en ligne. Une inscription ne sera finalisée qu’à la réception du paiement par chèques via envoie postal à la mairie(secrétaire du service culturel) ou par chèques/espèces directement auprès de la trésorière du l’office de la culture. Une confirmation par mail et sur le site sera envoyée au candidat.</p>
    <p>Chaque participant n'a le droit qu'à un compte et s'engage à ne pas réserver plusieurs chansons sous peine de discalification.</p>

    <p> Le candidat s'enregistrer préalablement pour pouvoir s'inscrire Une personne peut créer un compte et un seul.  Une réinitialisation du mot de passe avec un mot de passe par défaut sera possible en contactant le secrétariat.  Le candidat propose une chanson en précisant l'interprète et l’auteur et la durée. Après l'affichage d’un message  d'attente, il obtient une validation de son choix ou un rejet avec une invitation de choisir un autre titre</p>

    <p>Chaque participant interprétera sa chanson le jour même et le gagnant sera jugé par un jury de désigné par la ville de Longuenesse en fin de journée. La ville de Longuenesse tient à vous assurer l'impartialité totale du jury.</p>

    <h3>Les lots</h3>
    <p>A gagner : *La liste des dotations par ordre décroissant de valeur (nombre, description et prix unitaire TTC)<p>

    <p>La remise des lots se fera en mains propres à l'issu du concours par le maire de Longuenesse, quelle chance!</p>

